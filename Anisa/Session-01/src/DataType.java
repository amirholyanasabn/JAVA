public class DataType {
    public static void main(String[] args) {
        byte a = 1;
        short b = 2;
        int c = 1000;
        long d = 123355566l;

        float e = 1.23442f;
        double f = 2345.9894594;

        char g = 'a';
        char h = 65;
        System.out.println(g);
        System.out.println(h);
        System.out.println(Byte.MAX_VALUE);
    }
}
